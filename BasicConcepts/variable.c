#include <stdio.h>

int main() {
    int my_int = 23;
    float my_float = 23.0;
    char my_char = 'A';
    double my_double = 234.45463;

    printf("my_int: %d\n", my_int);
    printf("my_float: %f\n", my_float);
    printf("my_char: %c\n", my_char);
    printf("my_double: %f\n", my_double);

    return 0;
}
